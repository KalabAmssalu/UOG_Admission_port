    private void DeleteRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteRowActionPerformed
        if(FName.getText().isEmpty() || CName.getText().isEmpty() || Email.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No Student in the List      ");
        }
        else{
            
       try{
           
           String UName = FName.getText();
           
           String Que = "Delete FROM UOG.STUDENT where USERNAME='" + UName + "'";
           ss = con.createStatement();
           ss.executeUpdate(Que);
           select();
           JOptionPane.showMessageDialog(null, "Deleted Successfully press ok...     ");

       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null, ex);
       }
        }
    }//GEN-LAST:event_DeleteRowActionPerformed

