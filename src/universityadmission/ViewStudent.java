    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
         if(FName.getText().isEmpty() || CName.getText().isEmpty() || Email.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No Student in the List    ");
        }
        else{
        String login = "Update UOG.STUDENT set A_COUNTRY='"+ CName.getText()+"'"+", EMAIL='"+ Email.getText()+"'" +"where USERNAME='" + FName.getText()+"'";
       try{
           ss = con.createStatement();
           ss.executeUpdate(login);
           select();
           JOptionPane.showMessageDialog(null, "Update Successfully press ok...    ");

       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null, ex);
       }
         }
    }//GEN-LAST:event_editMouseClicked

